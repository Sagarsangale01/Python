# Generate arrays for basic mathematical operations

import numpy as np

array1 = np.arange(1,5)
array2 = np.arange(6,10)

print("Our First array: ", array1)
print("Our Second array: ", array2)

print("Array Operations on above array1 and array2: ")
print("Addition: ",array1 + array2)
print("Substration: ",array1 - array2)
print("Multiplication: ",array1 * array2)
print("Division: ",array1 / array2)

